 Cypress.Commands.add('login', (name, password) => { 
    cy.get('#login2').click({force: true})
        cy.get('#loginusername').type(name,{force: true})
        cy.wait(500)
        cy.get('#loginpassword').type(password,{force: true})
        cy.get('[onclick="logIn()"]').click()

  })
